package section_2_Intro_to_Java;

public class ArithmeticExample {
    public static void main(String[] args) {

        // Performing Arithmetic operations

        int a = 3;
        int b = 5;

        System.out.println("3 + 5 = " + (a + b));
        System.out.println("3 - 5 = " + (a - b));
        System.out.println("3 * 5 = " + (a * b));
        System.out.println("3 / 5 = " + (a / b));
        System.out.println("3 % 5 = " + (a % b));
    }
}
